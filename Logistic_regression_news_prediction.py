import numpy as np
import pandas as pd
import re
from nltk.corpus import stopwords   #Natural language toolkit (nltk)
from nltk.stem.porter import PorterStemmer
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score

import nltk
nltk.download('stopwords')

"""Printing the stop words in English"""

print(stopwords.words('english'))

"""Data preprocessing"""

#Load into pandasDF
news_dataset = pd.read_csv('train.csv')

news_dataset.shape

news_dataset.head()

#Counting the number of missing values in the datasets
news_dataset.isnull().sum()

#replacing the null values with emply strings
news_dataset = news_dataset.fillna('')

#merging the author name and news title
news_dataset['content'] = news_dataset['author']+' '+news_dataset['title']

news_dataset['content']

#Seperate the data & the lable
X = news_dataset.drop(columns='label', axis=1)
Y = news_dataset['label']

print(X)
print(Y)

"""Stemming

Stemming is the process of reducing a word to a root word

Ex: Actor, Actress, acting  --> Act
"""

port_stem = PorterStemmer()

def stemming(content):
    stemmed_content = re.sub('[^a-zA-Z]',' ',content)
    stemmed_content = stemmed_content.lower()
    stemmed_content = stemmed_content.split()
    stemmed_content = [port_stem.stem(word) for word in stemmed_content if not word in stopwords.words('english')]
    stemmed_content = ' '.join(stemmed_content)
    return stemmed_content

news_dataset['content'] = news_dataset['content'].apply(stemming)

print(news_dataset['content'])

#seperating the data and label
X = news_dataset['content'].values
Y = news_dataset['label'].values

print(X)
print(Y)

Y.shape

# Converting the textual data to numerical data
Vectorizer = TfidfVectorizer()
Vectorizer.fit(X)

X = Vectorizer.transform(X)

print(X)

"""Splitting the dataset to training and test"""

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, stratify=Y, random_state=2)

"""Training the model: Logistic regression"""

model = LogisticRegression()

model.fit(X_train,Y_train)

"""Evaluation

Accuracy score
"""

#Accuracy of the training data

X_train_prediction = model.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, Y_train)
print(X_train_prediction)

print('Accuracy score of the training data :',training_data_accuracy)

X_test_prediction = model.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, Y_test)
print(test_data_accuracy)

print('Accuracy score of the training data :',test_data_accuracy)

"""Building a predictive system"""

X_news = X_test[5]

prediction = model.predict(X_news)
print(prediction)

if (prediction[0]==0):
    print('The news is Real')
else:
    print('The news is Fake')

